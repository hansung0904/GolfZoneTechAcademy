package test.com;

import java.text.SimpleDateFormat;
import java.util.Date;

public class Test02Date2 {
	public static void main(String[] args) {
		System.out.println("Hello Date");

		Date ud = new Date();
		System.out.println(ud);
		System.out.println(ud.getTime()); // system.currenttime이랑 같은거~
		
		// 날짜타입을 이쁘게 변형(포멧) 해주는 객체
		SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss:SSS"); // parameter에 들어가 있는 형태로 변환해줌.
		String str_date = sdf.format(ud);
		System.out.println(str_date);
		
		
		

	

	} // end main
} // end class
